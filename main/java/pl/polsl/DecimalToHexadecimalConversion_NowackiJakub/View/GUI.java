    /**
     * Method for getting values from jTable.
     *
     * @return decimal values from jTable
     */
    public List<Integer> getTableValues() {

        List<Integer> tableInput = new ArrayList<>();
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            if (jTable1.getValueAt(i, 0) != null) {
                tableInput.add((Integer) jTable1.getValueAt(i, 0));
            }
        }
        return tableInput;//TODO: warunek jesli null + nie potwierdzaÄ‡ enterem!!!!!!
    }

    /**
     * Method for setting values in jTable.
     *
     * @param inputTable collection of input values
     * @param outputTable collection of converted values
     */
    public void setTableValues(List<Integer> inputTable, List<String> outputTable) {

        for (int i = 0; i < inputTable.size(); i++) {
            jTable1.setValueAt(inputTable.get(i), i, 0);
            jTable1.setValueAt(outputTable.get(i), i, 1);
        }

    }

